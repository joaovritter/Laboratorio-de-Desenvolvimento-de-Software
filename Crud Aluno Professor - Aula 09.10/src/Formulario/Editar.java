    private void btn_ConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ConsultarActionPerformed
        // TODO add your handling code here:
       if (rdo_Aluno.isSelected()){
        int idAluno = Integer.parseInt(txt_Id.getText());
        AlunoDAO aDAO = new AlunoDAO();

        Aluno a = aDAO.getAluno(idAluno);

        if (a==null){
            limparFormulario();
            JOptionPane.showMessageDialog(this, "Aluno nao encontrada!");
        }
        else {
            txt_Id.setText(String.valueOf(a.getId()));
            txt_Nome.setText(a.getNome());
            txt_Idade.setText(a.getIdade());
            txt_Curso.setText(a.getCurso());
            txt_Id2.setText(txt_Id.getText());
        }
      }
       
       if (rdo_Professor.isSelected()){
        int idProfessor = Integer.parseInt(txt_Id.getText());
        ProfessorDAO pDAO = new ProfessorDAO();

        Professor p = pDAO.getProfessor(idProfessor);

        if (p==null){
            limparFormulario();
            JOptionPane.showMessageDialog(this, "Aluno nao encontrada!");
        }
        else {
            txt_Id.setText(String.valueOf(p.getId()));
            txt_Nome.setText(p.getNome());
            txt_Idade.setText(p.getIdade());
            txt_Disciplina.setText(p.getDisciplina());
            txt_Id2.setText(txt_Id.getText());
        }
      }
    }//GEN-LAST:event_btn_ConsultarActionPerformed

