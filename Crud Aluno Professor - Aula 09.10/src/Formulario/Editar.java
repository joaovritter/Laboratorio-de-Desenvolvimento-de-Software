    private void btn_AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_AtualizarActionPerformed
       if(rdo_Aluno.isSelected()){
            Aluno a = new Aluno();
            a.setId (Integer.parseInt(txt_Id2.getText()));
            a.setNome(txt_Nome.getText());
            a.setIdade(txt_Idade.getText());
            a.setCurso(txt_Curso.getText());

            AlunoDAO aDAO = new AlunoDAO();
            aDAO.editar(a);
            limparFormulario();
       }
       
       if(rdo_Professor.isSelected()){
            Professor p = new Professor();
            p.setId (Integer.parseInt(txt_Id2.getText()));
            p.setNome(txt_Nome.getText());
            p.setIdade(txt_Idade.getText());
            p.setDisciplina(txt_Disciplina.getText());

            ProfessorDAO pDAO = new ProfessorDAO();
            pDAO.editar(p);
            limparFormulario();
       } 
    }//GEN-LAST:event_btn_AtualizarActionPerformed

    private void btn_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ExcluirActionPerformed
        // TODO add your handling code here:
        if (rdo_Aluno.isSelected()){
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Exclusão", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION){
                AlunoDAO aDAO = new AlunoDAO();
                aDAO.excluir(Integer.parseInt(txt_Id.getText()));
                limparFormulario();
            }
        }
        
        if (rdo_Professor.isSelected()){
            int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Exclusão", JOptionPane.YES_NO_OPTION);
            if (resposta == JOptionPane.YES_OPTION){
                ProfessorDAO pDAO = new ProfessorDAO();
                pDAO.excluir(Integer.parseInt(txt_Id.getText()));
                limparFormulario();
            }
        }

    }//GEN-LAST:event_btn_ExcluirActionPerformed

