    private void btSalvaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvaActionPerformed
       if(rbtAluno.isSelected()){
            Aluno a = new Aluno();
            
           a.setNome(txtNome.getText());
           a.setIdade(txtIdade.getText());
           a.setCurso(txtCurso.getText());
           
           AlunoDAO aDAO = new AlunoDAO();
           aDAO.inserir(a);
        }
        else if (rbtProfessor.isSelected()){
            Professor p = new Professor();
            
           p.setNome(txtNome.getText());
           p.setIdade(txtIdade.getText());
           p.setDisciplina(txtDiciplina.getText());
           
           ProfessorDAO pDAO = new ProfessorDAO();
           pDAO.inserir(p);
        }
        else 
            JOptionPane.showMessageDialog(null,
                    "Selecione uma categoria!!", "Erro!", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btSalvaActionPerformed

